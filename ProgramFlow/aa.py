x = 23

x += 1  # 24
print(x)

x -= 4  # 20
print(x)

x *= 5  # 100
print(x)

x //= 4  # 25
print(x)

x /= 5  # 5.0 float division
print(x)

x **= 2  # 25.0 still float
print(x)

x %= 5  # 0.0 exactly divisible by 5
print(x)

